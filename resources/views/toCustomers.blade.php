                    <h2>Шумозоляция</h2>

                    <p>При выборе межкомнатной двери следует обращать особое внимание на уровень шумоизоляции – это одна из важнейших характеристик. Что это означает на практике? Например, вы уложили ребенка спать, закрыли в его комнату дверь и можете вести полноценную жизнь или общаетесь шепотом и ходите на цыпочках.</p>

                    <h2>Факторы, влияющие на шумоизоляцию</h2>

                    <p>На нее влияет внутреннее содержание полотна – самый подходящий материал деревянный брус. Сотовый заполнитель значительно уступает по этим показателям дереву. Имеет значение и толщина плит HDF, облицовывающих полотно с двух сторон. HDF – это очень плотный материал, который не только защищает дерево от проникновения влаги, но и повышает шумоизоляцию двери. Компания ESTET в производстве своих дверей использует HDF до 6мм толщиной с каждой стороны. Нужный уровень шумоизоляции достигается еще и благодаря комплектации каждого остекленного полотна силиконовым герметиком, и комплектации коробочного бруса специальным шумоизоляционным уплотнителем.</p>

                    <h2>Долговечность</h2>

                    <p>Как уже было сказано выше, очень часто Покупатели в погоне за низкой стоимостью, упускают из вида качество и износостойкость материалов. Однако, завышенная цена двери не является гарантией высокого качества, важнее то, как двери изготавливались, ведь именно от этого процесса зависит их долговечность.</p>

                    <p>Не спешите заказывать двери из "ценных пород", ибо не все "ценные" являются долговечными. В первую очередь, следует обращать внимание на прочность покрытия. Если такие двери изготовлены с соблюдением всех тонкостей технологии, то они переживут и вас, и ваших внуков.</p>

                    <h2>Износостойкость</h2>

                    <p>Наиболее распространенная ошибка - приобретение для офисов и общественных помещений так называемых "бытовых" дверей. Устойчивость к истиранию и прочим механическим воздействиям позволяет устанавливать двери фабрики ESTET в помещениях с высокой проходимостью людей – общественные учреждения, офисы и магазины.</p>

                    <p>Но и для дома устойчивость двери к внешним воздействиям, а именно когтям животных, сколам, царапинам и трещинам просто необходима.</p>

                    <p>На сегодняшний день покрытие PVC гораздо более устойчиво к механическим повреждениям, чем натуральный шпон или, тем более, бумага, которую часто используют для ламинирования при производстве дверей самой низкой ценовой категории.</p>

                    <p>Так же повышенной износостойкостью обладают следующие материалы: Ламинатин (CPL-пластик) и ЭКО-шпон (древесные волокна с добавлением связующего клея). Основным преимуществом PVC-покрытия перед этими материалами является – эластичность (при ударе не образуется трещин и сколов).</p>

                    <h2>Влагостойкость</h2>

                    <p>Устойчивость к воздействию влаги – одна из основных характеристик долговечной межкомнатной двери. На сегодняшний момент появился целый ряд искусственных материалов, по своим характеристикам значительно превосходящих натуральные и доступных по цене.</p>

                    <p>Одним из таких материалов является покрытие Кортекс – это немецкий материал нового поколения, по цвету и текстуре не отличим от натурального шпона. Дверные полотна с такой отделкой исключительно удобны в быту – позволяют производить влажную обработку поверхности, в том числе и активными чистящими жидкостями, так же эти двери идеальны для влажных помещений – кухня, ванная, сан. узел.</p>

                    <h2>Коробление</h2>

                    <p>Качественные межкомнатные двери не подвержены короблению, даже при условии ощутимых перепадов температуры и влажности. Для того что бы предотвратить коробление древесины, полотна должны быть облицованы с обеих сторон плитами HDF.</p>

                    <p>HDF (с англ. High Density Fiberboard - древесноволокнистая плита высокой плотности) – это плотноспрессованные древесные волокна, без добавления клея. Скрепляющим веществом является – лигнин (выделяется из древесины при нагревании).</p>

                    <h2>Удобство эксплуатации</h2>

                    <p>При выборе остекленных дверей необходимо обращать внимание на следующие нюансы:</p>

                    <div class="buyers-text-inner">
                        <div class="buyers-text-inner-title">Плотное матирование</div>

                        <p>Желательно чтобы стекло матировалось с двух сторон, что позволяет устанавливать двери со стеклом в спальни и сан. узлы</p>

                        <div class="buyers-text-inner-title">Антипальчиковое покрытие</div>

                        <p>Стекло обрабатывается специальным раствором жироотталкивающими свойствами, что значительно облегчает уход за матированным стеклом, легко проверить наличие такого защитного покрытия - достаточно просто прикоснуться к стеклу ладонью</p>

                        <div class="buyers-text-inner-title">Силиконовый герметик</div>

                        <p>Стекло обрабатывается по периметру прозрачным силиконовым герметиком, для того. Его преимущества:</p>

