                            <li>стекло в процессе эксплуатации не дребезжит;</li>
                            <li>помещение защищено от проникновения посторонних запахов;</li>
                            <li>дополнительная шумоизоляция;</li>
                            <li>чем лучше стекло зафиксировано, тем сложнее его разбить.</li>
