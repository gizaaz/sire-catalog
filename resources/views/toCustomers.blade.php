                    <h2>Экологичность и безопасность</h2>

                    <p>Безопасность продукции - один из главных аспектов. Все материалы, используемые в производстве должны иметь гигиенические сертификаты.</p>

                    <p>Существует ряд мифов и заблуждений о том, что самые безопасные межкомнатные двери из массива дерева либо с покрытием натуральный шпон. Не стоит забывать, что такие двери покрываются несколькими слоями лакокрасочных материалов. Зачастую недобросовестные производители используют лаки на основе фенола и других, вредных для здоровья веществ.</p>

                    <p>Для облицовки полотен фабрика ESTET использует материал PVC. Он не содержит и не выделяет никаких вредоносных веществ. В нем не образуются микротрещины, что препятствует размножению вредоносных микроорганизмов (грибка и плесени).</p>

